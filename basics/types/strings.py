# SINGLE QUOTE
print('Hello')
print('She said "I want this"')

# DOUBLE QUOTE
print("Hello")
print("Don't do that")

# TAKE A QUOTE AS A STRING (ESCAPING)
print('She said "Don\'t do that"d')

# CONVER INT TO STRING str()
print("This costs " + str(6) + " dolars")

# CONCAT
print("This costs " + str(6+5) + " dolars")
print("This costs " + "sixty" + " dolars")

# SEPARATE split("")
print("Hello:Nick:World".split(":"))
print("My name is " + "Hello:Nick:World".split(":")[1])
